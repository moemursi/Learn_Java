public class Employee {
   private int id;
   private double salary;

   //get id of employee
    public int getId() {
        return id;
    }

    //set id of employee
    public void setId(int idNum) {
        this.id = idNum;

    }

    //get salary of employee
    public double getSalary() {
        return salary;
    }

    //set salary of employee
    public void setSalary(double sal) {
        this.salary = sal;
    }




}